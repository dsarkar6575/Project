        String roll=jTextField5.getText();
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
           Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/srms", "root", "1234");
           Statement st=con.createStatement();
           
           ResultSet rs=st.executeQuery("select * from student inner join result where student.rollno='"+roll+"' and result.rollno='"+roll+"'");
  
           if(rs.next())
           {
               jTextField2.setText(rs.getString(2));
               jTextField3.setText(rs.getString(3));
               jTextField4.setText(rs.getString(4));
               jTextField6.setText(rs.getString(6));
               jTextField1.setText(rs.getString(5));
               jTextField7.setText(rs.getString(8));
               jTextField10.setText(rs.getString(9));
               jTextField8.setText(rs.getString(10));
               jTextField9.setText(rs.getString(11));
               
               int s1=Integer.parseInt(jTextField7.getText());
               String totalmarks=String.valueOf(s1);
               jTextField11.setText(totalmarks);
           }
        }
